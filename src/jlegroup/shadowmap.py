# -*- coding: utf-8 -*-
"""shadowmap — occultation shadow maps on the Earth.

Modern implementation of the functionality of the Mathematica
``jleGroup`shadowMap``` package (4.1.4, 2013): given a star and a UT
time, draw the hemisphere of the Earth facing the star — the view from
the occulting body — with coastlines, night-side shading (sun more than
a chosen angle below the horizon), and the occultation shadow track:
straight lines in the fundamental plane at the closest-approach offset
``dist`` (Earth radii), position angle ``pa`` (degrees, N through E),
band half-width ``radius`` (occulting-body radius in Earth radii), and
dashed prediction-error lines.  Mercator and equirectangular maps in
view-frame coordinates are also provided.  Function mapping:

    globe                       smGlobe
    substar_point               smGlobe's view point (via ICRS->ITRS)
    subsolar_point / antisolar_point   smSunPosn's role (via get_sun)
    night_polygon               showShadow / newshad
    coastline_outlines          newcont
    shadow_tracks               drawtracks (+ opPrintDiagnostic notes)
    track_ground_paths          (new: the on-Earth ground tracks of the
                                 shadow lines, drawn on cylindrical maps)
    dist_from_impact_parameter  smDist
    offset_prediction           smOffset

Design: superior implementations preferred over porting (maintainer
direction, 2026-07-14).  Concretely:

* **Time and coordinates: astropy.**  Times are ``astropy.time.Time``
  (UTC in, proper UT1/TT handled internally); star positions are
  ``SkyCoord`` (any frame — catalog ICRS is the natural input, unlike
  the original's apparent-of-date strings).  The sub-star point comes
  from transforming the star direction to ITRS (full precession,
  nutation, aberration, Earth rotation — sub-arcsecond class, replacing
  the original's IAU-1982 GMST + apparent-coordinates assumption); the
  sub-solar point from ``astropy.coordinates.get_sun`` (arcsecond
  class, replacing the ~arcminute Duffett-Smith-style theory in
  smSunPosn).  Earth-rotation accuracy is governed by astropy's IERS
  configuration; with no IERS data (offline, or far-future dates)
  astropy degrades gracefully per ``astropy.utils.iers.conf``.

* **Coastlines: Natural Earth.**  The bundled outline database is the
  public-domain Natural Earth 1:110m coastline (134 polylines, 5128
  vertices; naturalearthdata.com, via the nvkelso/natural-earth-vector
  GitHub repository, fetched 2026-07-14), stored as
  ``jlegroup/data/ne_110m_coastline.npz`` — replacing the ~2100-vertex
  hand-digitized database embedded in the Mathematica package.
  Polylines are properly split at the visible-hemisphere limb and at
  the view-frame ±180 deg meridian (the original dropped whole
  polylines instead).

* **Geometry engine: ported and corrected.**  The view rotation,
  orthographic/mercator/equirectangular projections, night-cap
  construction and hemisphere clipping, track-line construction, and
  the smDist/smOffset methods are ported from the Mathematica source
  (validated against reference output generated by running the
  original; see tests/data/shadowmap-mathematica/).  The original's
  night-shadow placement defect is NOT ported: shadowMap 4.1.4 builds
  the antisolar direction with a sign error that misplaces the night
  cap in view longitude whenever both the view latitude and the solar
  declination are nonzero (up to tens of degrees of terminator error in
  unfavorable geometry — established during validation, see the data
  README).  Here the cap is centered on the true antisolar point, and
  the drawn terminator sits at sun altitude ``-horizon_angle`` to
  machine precision of the spherical model.

Model and units
---------------
The Earth is a sphere of unit radius (display model, as in the
original): latitudes are geocentric, the horizon is geometric (no
refraction — fold refraction into ``horizon_angle`` if wanted, ~0.57
deg), and the terminator is computed from the geocentric apparent sun.
Angles at the API are degrees unless a name says otherwise; fundamental
plane distances are in Earth radii; ``b`` in arcsec, body distance in
au, sky velocity in km/s.  Earth's radius and the au come from
``jlegroup.physicalData`` (BODIES["Earth"].radius_km = 6378.14, the
IAU-1976 value the original hardcoded; AU_KM is DE-405, 1.42e-5
relative above the original's rounded 1.496e8 — visible only in
dist_from_impact_parameter and offset_prediction timing).  The
orthographic map is the view from the star: north up, east right.

Provenance
----------
Geometry/track/smDist/smOffset algorithms ported from Mathematica
jleGroup shadowMap 4.1.4 (MIT license, lineage permissions complete
2026-07-11); astropy replaces jleGroup`timeAndCoords` and smSunPosn;
coastline data is Natural Earth (public domain).  No reference
publication exists for this module; validation is against the original
package's computed output where the port is exact (tracks, smDist,
smOffset, compass directions) and at the superseded theories' accuracy
class where astropy replaces them (see tests/test_shadowmap.py).
"""

