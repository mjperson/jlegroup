def _night_cap(horizon_angle_deg, n=203):
    """Boundary of the night cap as (lat, lon) around (0, 0): the circle
    of angular radius (90 - horizon_angle) deg (closed: the last point
    repeats the first)."""
    t = np.arange(n) * (2.0 * math.pi / (n - 1))
    s = math.sin(math.radians(90.0 - horizon_angle_deg))
    lat = np.arcsin(s * np.sin(t))
    lon = np.arcsin(np.clip(s * np.cos(t) / np.cos(lat), -1.0, 1.0))
    return lat, lon


def _clip_to_hemisphere(pts, side):
    """Clip a closed view-frame polygon to |lon'| < pi/2, closing it along
    the limb through the pole on the ``side`` (+1 north / -1 south) of
    the cap center.  Ported from the original clipgeneral (which errored
    on an entirely hidden polygon; this returns empty)."""
    inr = np.abs(pts[:, 1]) < math.pi / 2.0
    if inr.all():
        return pts
    if not inr.any():
        return np.empty((0, 2))
    inr_ext = np.append(inr, inr[0])
    trans = np.nonzero(inr_ext[:-1] != inr_ext[1:])[0]
    p1, p2 = int(trans[0]), int(trans[1])
    edges = []
    for k in (p1, p2):
        lat0 = pts[k, 0]
        lon_sign = math.copysign(1.0, pts[k, 1]) if pts[k, 1] != 0 else 0.0
        arc = np.linspace(lat0, side * math.pi / 2.0, 159)
        edges.append(np.column_stack(
            [arc, np.full(len(arc), math.pi / 2.0 * lon_sign)]))
    if not inr_ext[p1]:
        edges[0] = edges[0][::-1]
    else:
        edges[1] = edges[1][::-1]
    firstone = bool(inr_ext[0])
    nexttolast = bool(inr_ext[-2])
    n = len(pts)
    if firstone and nexttolast:
        return np.vstack([pts[:p1 + 1], edges[0], edges[1], pts[p2:n]])
    if not firstone and not nexttolast:
        return np.vstack([edges[0], pts[p1:p2 + 1], edges[1]])
    if firstone and not nexttolast:
        return np.vstack([pts[:p1 + 1], edges[0], edges[1]])
    return np.vstack([edges[0], pts[p1:p2 + 1], edges[1]])


def night_polygon(view_lat_deg, view_lon_deg, antisolar_lat_deg,
                  antisolar_lon_deg, horizon_angle_deg=0.0,
                  projection="orthographic"):
    """The night-side polygon in projected plot coordinates.

    The region where the sun is more than ``horizon_angle_deg`` below
    the geometric horizon — the spherical cap of angular radius
    (90 - h) deg centered on the antisolar point — rotated into the
    view frame, clipped to the visible hemisphere (orthographic), and
    projected.  Returns an (N, 2) array; empty if no part of the night
    side faces the star.

    The high-level ``globe`` computes the antisolar point with astropy;
    this function is pure spherical geometry (testable in isolation).

    Cylindrical projections return the night region as a single closed
    polygon in CONTINUOUS (unwrapped) view longitude: x may extend
    beyond ±pi, and when the cap encloses a view-frame pole the polygon
    is closed along that pole's map edge (two extra vertices appended
    after the 203 boundary points).  The region tiles with period 2*pi
    in x — draw it at x and x ± 2*pi, as ``globe`` does, and any part
    beyond the map limits clips away.
    """
    _check_projection(projection)
    vf_lat, vf_lon = view_rotation(math.radians(antisolar_lat_deg),
                                   math.radians(antisolar_lon_deg),
                                   view_lat_deg, view_lon_deg)
    vf_lat = float(vf_lat)
    vf_lon = float(vf_lon)
    # Tilt the (0,0)-centered cap so its center lands on the antisolar
    # view-frame position: rotate in latitude by vf_lat, then shift the
    # view-frame longitude by vf_lon.
    alpha = math.pi / 2.0 + vf_lat
    sa, ca = math.sin(alpha), math.cos(alpha)
    lat_c, lon_c = _night_cap(horizon_angle_deg)
    latp = np.arcsin(np.clip(sa * np.sin(lat_c)
                             - ca * np.cos(lat_c) * np.cos(lon_c), -1.0, 1.0))
    lonp = np.arctan2(np.cos(lat_c) * np.sin(lon_c),
                      sa * np.cos(lat_c) * np.cos(lon_c)
                      + ca * np.sin(lat_c)) + vf_lon
    lonp = np.where(np.abs(lonp) > math.pi,
                    lonp - np.sign(lonp) * 2.0 * math.pi, lonp)
    if projection == "orthographic":
        side = math.copysign(1.0, vf_lat) if vf_lat != 0 else 1.0
        clipped = _clip_to_hemisphere(np.column_stack([latp, lonp]), side)
        if len(clipped) == 0:
            return clipped
        return _project(clipped[:, 0], clipped[:, 1], "orthographic")
    # Cylindrical: unwrap the boundary so the polygon is contiguous even
    # when the cap crosses the ±pi view meridian.  The closed boundary
    # comes back to its start (closure ~ 0) unless the cap encloses a
    # view-frame pole (closure ±2*pi) — then close along that pole's map
    # edge (clamped short of the pole on the mercator map, whose y
    # diverges there; the fill clips at the axes limits anyway).
    lonu = np.unwrap(lonp)
    lat_out, lon_out = latp, lonu
    if abs(lonu[-1] - lonu[0]) > math.pi:
        pole_sign = math.copysign(1.0, vf_lat)
        pole_lat = pole_sign * (math.pi / 2.0
                                if projection == "equirectangular"
                                else math.pi / 2.0 - 1e-3)
        lat_out = np.concatenate([latp, [pole_lat, pole_lat]])
        lon_out = np.concatenate([lonu, [lonu[-1], lonu[0]]])
    # representative copy nearest the principal window
    lon_out = lon_out - round(float(np.mean(lon_out))
                              / (2.0 * math.pi)) * 2.0 * math.pi
    return _project(lat_out, lon_out, projection)


