_coastlines_cache = None


def coastlines():
    """The bundled coastline database: a list of 134 polylines, each an
    (N, 2) array of (lat, lon) in radians.

    Natural Earth 1:110m "Coastline" (public domain,
    naturalearthdata.com), bundled as
    jlegroup/data/ne_110m_coastline.npz.  Do not mutate the returned
    arrays.
    """
    global _coastlines_cache
    if _coastlines_cache is None:
        ref = resources.files("jlegroup").joinpath(
            "data/ne_110m_coastline.npz")
        with ref.open("rb") as f:
            z = np.load(f)
            latlon = np.radians(z["latlon_deg"].astype(np.float64))
            offsets = z["offsets"]
        _coastlines_cache = [latlon[a:b]
                             for a, b in zip(offsets[:-1], offsets[1:])]
    return _coastlines_cache


def _split_runs(mask):
    """Start/stop index pairs of the True runs of a boolean array."""
    idx = np.nonzero(np.diff(mask.astype(np.int8)))[0] + 1
    bounds = np.concatenate([[0], idx, [len(mask)]])
    return [(int(a), int(b)) for a, b in zip(bounds[:-1], bounds[1:])
            if mask[a]]


def coastline_outlines(view_lat_deg, view_lon_deg, projection="orthographic"):
    """Coastlines in the view frame, projected: a list of (N, 2) arrays.

    Orthographic: polylines are split at the limb and only the runs on
    the visible hemisphere are kept.  Cylindrical projections: polylines
    are split where they cross the view-frame ±180 deg meridian.  (The
    original culled whole polylines in both situations; splitting is
    required for the long Natural Earth arcs.)
    """
    _check_projection(projection)
    out = []
    for pts in coastlines():
        latp, lonp = view_rotation(pts[:, 0], pts[:, 1],
                                   view_lat_deg, view_lon_deg)
        if projection == "orthographic":
            pieces = _split_runs(np.abs(lonp) < math.pi / 2.0)
        else:
            wraps = np.abs(np.diff(lonp)) > math.pi
            cuts = np.concatenate([[0], np.nonzero(wraps)[0] + 1,
                                   [len(lonp)]])
            pieces = list(zip(cuts[:-1], cuts[1:]))
        for a, b in pieces:
            if b - a >= 2:
                out.append(_project(latp[a:b], lonp[a:b], projection))
    return out


