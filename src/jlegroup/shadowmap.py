#: Compass names, verbatim from the original directionList — including the
#: trailing spaces, which the diagnostic notes rely on.
_DIRECTIONS = ("North ", "North-East ", "East ", "South-East ",
               "South ", "South-West ", "West ", "North-West ")


def find_direction(pa_deg):
    """Compass name of a position angle (the returned string keeps the
    original's trailing space)."""
    return _DIRECTIONS[int(((pa_deg + 22.5) % 360.0) // 45.0)]


def opposite_direction(direction):
    """The opposite compass name."""
    return _DIRECTIONS[(_DIRECTIONS.index(direction) + 4) % 8]


@dataclass(frozen=True)
class TrackSet:
    """Shadow-track lines in the fundamental plane (Earth radii, in the
    orthographic view: north up, east right).

    near, center, far : (2, 2) arrays — the two endpoints of the shadow
        edge nearer Earth center, the shadow center line, and the far
        edge (separated by ``radius`` each).
    error_minus, error_plus : (2, 2) arrays — the dashed prediction-
        error lines at near - pred_error and far + pred_error (equal to
        near/far when pred_error is 0).
    note : diagnostic message ("" when the track crosses cleanly),
        verbatim from the original including its line breaks.
    """

    near: np.ndarray
    center: np.ndarray
    far: np.ndarray
    error_minus: np.ndarray
    error_plus: np.ndarray
    note: str

    def lines(self):
        """The three solid lines, near/center/far."""
        return [self.near, self.center, self.far]


@dataclass(frozen=True)
class TrackPaths:
    """Ground tracks of the shadow lines, as view-frame (lat', lon')
    curves — the fundamental-plane lines of TrackSet lifted onto the
    star-facing hemisphere (inverse orthographic projection).

    Each field is an (n, 2) array [lat', lon' radians] spanning limb to
    limb, or None where that line misses the Earth entirely.  Convert to
    geographic coordinates with ``view_rotation_inverse(path[:, 0],
    path[:, 1], view_lat, view_lon)`` — the ground coordinates of the
    predicted path.  ``note`` matches shadow_tracks.
    """

    near: np.ndarray | None
    center: np.ndarray | None
    far: np.ndarray | None
    error_minus: np.ndarray | None
    error_plus: np.ndarray | None
    note: str

    def lines(self):
        """The three solid-line paths, near/center/far (None entries
        included)."""
        return [self.near, self.center, self.far]


def track_ground_paths(dist=0.0, pa_deg=0.0, radius=0.0, pred_error=0.0,
                       n=181):
    """Ground tracks of the five shadow lines (see shadow_tracks for the
    parameters): each fundamental-plane line at perpendicular offset d
    from Earth center crosses the disk where |d| < 1; the on-disk chord,
    lifted to the star-facing hemisphere, is the locus of surface points
    swept by that shadow edge.  Returns a TrackPaths of view-frame
    (lat', lon') curves (None for lines with |d| >= 1).

    This is what the cylindrical projections of ``globe`` draw — the
    original package drew the raw fundamental-plane lines onto those
    maps in map coordinates, which is not meaningful; ground tracks are.
    """
    theta = math.pi / 2.0 - math.radians(pa_deg)
    ct, st = math.cos(theta), math.sin(theta)
    offsets = {
        "near": dist - radius,
        "center": dist,
        "far": dist + radius,
        "error_minus": dist - radius - pred_error,
        "error_plus": dist + radius + pred_error,
    }
    paths = {}
    for name, off in offsets.items():
        if abs(off) >= 1.0:
            paths[name] = None
            continue
        half = math.sqrt(1.0 - off * off)
        s = np.linspace(-half, half, n)
        x = off * ct - s * st
        y = off * st + s * ct
        latp = np.arcsin(np.clip(y, -1.0, 1.0))
        lonp = np.arcsin(np.clip(x / np.cos(latp), -1.0, 1.0))
        paths[name] = np.column_stack([latp, lonp])
    return TrackPaths(note=shadow_tracks(dist, pa_deg, radius,
                                         pred_error).note, **paths)


def shadow_tracks(dist=0.0, pa_deg=0.0, radius=0.0, pred_error=0.0):
    """Shadow-track geometry (ported from drawtracks; validated exact
    against the original).

    dist : closest-approach distance of the shadow CENTER from Earth
        center, Earth radii (see dist_from_impact_parameter).
    pa_deg : position angle of the occulting body relative to the star,
        deg N through E.
    radius : occulting-body radius, Earth radii.
    pred_error : 1-sigma prediction uncertainty, Earth radii.
    """
    theta = math.pi / 2.0 - math.radians(pa_deg)
    ct, st = math.cos(theta), math.sin(theta)
    subdist = dist - radius
    direc = find_direction(pa_deg)
    note = ""
    if subdist > 1.0:
        note = (" No tracks cross the earth\n "
                + "All three tracks are " + direc + "of the earth")
        xm, ym = subdist * ct, subdist * st
        x1 = xm + 1.3 * math.cos(theta + math.pi / 2.0)
        y1 = ym + 1.3 * math.sin(theta + math.pi / 2.0)
    elif subdist <= 0.0:
        if subdist >= -1.0 and dist <= 1.0 and dist + radius > 1.0:
            note = " One track is " + direc + "of the earth"
        elif subdist >= -1.0 and dist > 1.0:
            note = " Two tracks are " + direc + "of the earth"
        elif subdist < -1.0 and dist < 1.0:
            note = (" The entire earth is in the shadow \n "
                    + "One track is " + direc + "of the earth, \n "
                    + "and one track is " + opposite_direction(direc)
                    + "of the earth")
        elif subdist < -1.0 and dist > 1.0:
            note = (" The entire earth is in the shadow \n "
                    + "No tracks cross the earth \n"
                    + " Two tracks are " + direc + "of the earth, \n "
                    + "and one track is " + opposite_direction(direc)
                    + "of the earth")
        xm, ym = subdist * ct, subdist * st
        x1 = xm + 1.3 * math.cos(math.pi / 2.0 + theta)
        y1 = ym + 1.3 * math.sin(math.pi / 2.0 + theta)
    else:
        if dist <= 1.0 and dist + radius > 1.0:
            note = " One track is " + direc + "of the earth"
        elif dist > 1.0:
            note = " Two tracks are " + direc + "of the earth"
        pdist = subdist
        xm, ym = pdist * ct, pdist * st
        a = math.sqrt(abs((1.0 + pdist) * (1.0 - pdist)))
        augmented = a + 0.3
        alpha = math.atan2(augmented, pdist)
        psi = theta + alpha
        hypot = math.sqrt(augmented * augmented + pdist * pdist)
        x1, y1 = hypot * math.cos(psi), hypot * math.sin(psi)
    x2, y2 = 2.0 * xm - x1, 2.0 * ym - y1
    near = np.array([[x1, y1], [x2, y2]])
    shift = np.array([radius * ct, radius * st])
    err = np.array([pred_error * ct, pred_error * st])
    return TrackSet(near=near,
                    center=near + shift,
                    far=near + 2.0 * shift,
                    error_minus=near - err,
                    error_plus=near + 2.0 * shift + err,
                    note=note)


