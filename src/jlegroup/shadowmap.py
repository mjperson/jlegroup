def star_coord(ra, dec=None):
    """Coerce flexible star input to a SkyCoord.

    Accepts a ready SkyCoord (any frame, returned unchanged); a single
    string "HH MM SS[.s] ±DD MM SS[.s]" (colon or space separated); or
    separate ``ra``, ``dec`` as sexagesimal strings (RA in hours, Dec in
    degrees, the occultation-community convention), Angles/Quantities,
    or plain floats **in degrees**.  Bare values are taken as ICRS.
    """
    if isinstance(ra, SkyCoord):
        return ra
    if dec is None:
        if not isinstance(ra, str):
            raise TypeError("single-argument star input must be a SkyCoord "
                            "or an 'RA DEC' string")
        return SkyCoord(ra, unit=(u.hourangle, u.deg), frame="icrs")
    if isinstance(ra, str):
        ra = Angle(ra, unit=u.hourangle)
    elif not isinstance(ra, u.Quantity):
        ra = ra * u.deg
    if isinstance(dec, str):
        dec = Angle(dec, unit=u.deg)
    elif not isinstance(dec, u.Quantity):
        dec = dec * u.deg
    return SkyCoord(ra=ra, dec=dec, frame="icrs")


def as_time(time):
    """Coerce flexible time input to an astropy Time (UTC).

    Accepts a ready Time (returned unchanged); a float (MJD, UTC — the
    package's time currency); a datetime; or a string — ISO formats via
    astropy, plus the legacy jleGroup 6-field form
    "YYYY:MM:DD:HH:MM:SS" with separators colon, space, comma, or slash
    (day 0 and overflowing fields roll over, matching the Mathematica
    date-list convention, e.g. "1990,1,0,0,0,0" = 1989-12-31).
    """
    if isinstance(time, Time):
        return time
    if isinstance(time, (int, float)):
        return Time(float(time), format="mjd", scale="utc")
    if isinstance(time, str):
        try:
            return Time(time, scale="utc")
        except ValueError:
            fields = [p for p in re.split(r"[\s:,/]+", time.strip()) if p]
            if len(fields) == 6:
                y, mo = int(float(fields[0])), int(float(fields[1]))
                d, h, mi, s = (float(x) for x in fields[2:])
                dt = _datetime(y, mo, 1) + _timedelta(
                    days=d - 1.0, hours=h, minutes=mi, seconds=s)
                return Time(dt, scale="utc")
            raise ValueError(f"unrecognized time string: {time!r}") from None
    return Time(time, scale="utc")


def _wrap_deg(lon_deg):
    """Wrap a longitude to (-180, 180]."""
    return -((-lon_deg + 180.0) % 360.0 - 180.0)


def _itrs_direction_latlon(coord, time):
    """Geocentric (lat_deg, east_lon_deg) of the ITRS direction of a coord."""
    itrs = coord.transform_to(ITRS(obstime=time))
    sph = itrs.spherical
    return float(sph.lat.to_value(u.deg)), \
        _wrap_deg(float(sph.lon.to_value(u.deg)))


def substar_point(star, time):
    """Sub-star point (lat_deg, east_lon_deg): where the star is at zenith.

    The star direction transformed to the Earth-fixed ITRS frame at the
    given UT — precession, nutation, aberration, and Earth rotation all
    handled by astropy.  This is the center of the orthographic view.
    """
    return _itrs_direction_latlon(star_coord(star), as_time(time))


def subsolar_point(time):
    """Sub-solar point (lat_deg, east_lon_deg) from the apparent sun."""
    t = as_time(time)
    return _itrs_direction_latlon(get_sun(t), t)


def antisolar_point(time):
    """Antisolar point (lat_deg, east_lon_deg): the center of the night."""
    lat, lon = subsolar_point(time)
    return -lat, _wrap_deg(lon + 180.0)


# ---------------------------------------------------------------------------
# View geometry (ported from shadowMap 4.1.4 xcont/ycont, validated exact)
# ---------------------------------------------------------------------------


def view_rotation(lat, lon, view_lat_deg, view_lon_deg):
    """Rotate earth (lat, lon) [radians] into the view frame centered on
    (view_lat_deg, view_lon_deg): returns (lat', lon') with the view
    center at (0, 0); the visible hemisphere is |lon'| < pi/2.
    Accepts scalars or arrays.
    """
    alpha = math.pi / 2.0 - math.radians(view_lat_deg)
    lam0 = math.radians(view_lon_deg)
    sa, ca = math.sin(alpha), math.cos(alpha)
    d = np.asarray(lon) - lam0
    lat = np.asarray(lat)
    latp = np.arcsin(np.clip(sa * np.sin(lat) - ca * np.cos(lat) * np.cos(d),
                             -1.0, 1.0))
    lonp = np.arctan2(np.cos(lat) * np.sin(d),
                      sa * np.cos(lat) * np.cos(d) + ca * np.sin(lat))
    return latp, lonp


def view_rotation_inverse(latp, lonp, view_lat_deg, view_lon_deg):
    """Inverse of view_rotation: view-frame (lat', lon') -> earth (lat, lon)."""
    alpha = math.pi / 2.0 - math.radians(view_lat_deg)
    lam0 = math.radians(view_lon_deg)
    sa, ca = math.sin(alpha), math.cos(alpha)
    latp = np.asarray(latp)
    lonp = np.asarray(lonp)
    vx = np.cos(latp) * np.cos(lonp)
    vy = np.cos(latp) * np.sin(lonp)
    vz = np.sin(latp)
    lat = np.arcsin(np.clip(ca * vx + sa * vz, -1.0, 1.0))
    lon = lam0 + np.arctan2(vy, sa * vx - ca * vz)
    return lat, lon


def _mercator_y(lat):
    """Mercator ordinate y = log tan(lat/2 + pi/4)."""
    return np.log(np.tan(np.asarray(lat) / 2.0 + math.pi / 4.0))


def _check_projection(projection):
    if projection not in PROJECTIONS:
        raise ValueError(
            f"unknown projection {projection!r}; choose from {PROJECTIONS}")


def _project(latp, lonp, projection):
    """View-frame (lat', lon') arrays -> (N, 2) plot coordinates."""
    if projection == "orthographic":
        return np.column_stack([np.cos(latp) * np.sin(lonp), np.sin(latp)])
    if projection == "mercator":
        return np.column_stack([lonp, _mercator_y(latp)])
    return np.column_stack([lonp, latp])


