def dist_from_impact_parameter(b_arcsec, distance_au):
    """Impact parameter (arcsec) and geocentric distance (au) -> shadow
    center offset in Earth radii (smDist).

    Uses physicalData BODIES["Earth"].radius_km and AU_KM (the original
    hardcoded 6378.14 km and the rounded au 1.496e8 km; the au
    difference is 1.42e-5 relative).
    """
    r_km = physicalData.BODIES["Earth"].radius_km
    d_km = distance_au * physicalData.AU_KM
    radius_arcsec = math.atan2(r_km, d_km) * (3600.0 * 180.0 / math.pi)
    return b_arcsec / radius_arcsec


@dataclass(frozen=True)
class OffsetResult:
    """Revised occultation prediction from offset_prediction.

    coord : offset-applied star position (ICRS SkyCoord).
    time : revised closest-approach Time (UT).
    b_arcsec : revised impact parameter.
    pa_deg : revised position angle, deg N through E.
    ra_string, dec_string, date_string : formatted conveniences.
    """

    coord: SkyCoord
    time: Time
    b_arcsec: float
    pa_deg: float

    @property
    def ra_string(self):
        return self.coord.ra.to_string(unit=u.hourangle, sep=" ",
                                       precision=4, pad=True)

    @property
    def dec_string(self):
        return self.coord.dec.to_string(unit=u.deg, sep=" ", precision=4,
                                        pad=True, alwayssign=True)

    @property
    def date_string(self):
        return self.time.utc.iso


def offset_prediction(ra, dec, ra_offset, dec_offset, b_arcsec, pa_deg,
                      time, sky_velocity_kms, distance_au,
                      retrograde=False, add_offset_to_star=False):
    """Revise an occultation prediction for a star-position offset without
    re-running the search (the smOffset method: linear relative motion).

    ra, dec : original star position (see star_coord; strings are RA
        hours / Dec degrees sexagesimal).
    ra_offset, dec_offset : the astrometric offset — sexagesimal strings
        (RA offset in HOURS "HH MM SS.ss", Dec offset in degrees, as in
        the original), Angles/Quantities, or floats in DEGREES.  By
        default the offset is SUBTRACTED from the star (equivalent to
        adding it to the body); add_offset_to_star=True adds it instead.
    b_arcsec, pa_deg, time : original closest-approach impact parameter
        (nonzero), position angle, and UT time (see as_time).
    sky_velocity_kms, distance_au : relative sky-plane speed and
        geocentric distance of the body.
    retrograde : True if the body's apparent motion is retrograde
        (flips the sign of the time correction).

    Approximate by construction (the original documents up to ~0.009"
    in b, 1-2 deg in PA, and seconds in time versus a full re-search).
    """
    star = star_coord(ra, dec)
    t0 = as_time(time)

    def _angle(x, default_unit):
        if isinstance(x, str) or isinstance(x, u.Quantity):
            return Angle(x, unit=default_unit) if isinstance(x, str) \
                else Angle(x)
        return Angle(x, unit=u.deg)

    ra_off = _angle(ra_offset, u.hourangle)
    dec_off = _angle(dec_offset, u.deg)
    sign = -1.0 if add_offset_to_star else 1.0
    s_ra_off = sign * ra_off.to_value(u.rad)
    s_dec_off = sign * dec_off.to_value(u.rad)
    dec_rad = star.dec.to_value(u.rad)
    new_star = SkyCoord(ra=star.ra - sign * ra_off,
                        dec=star.dec - sign * dec_off, frame="icrs")
    pa_rad = math.radians(pa_deg)
    b_rad = b_arcsec * math.pi / (3600.0 * 180.0)
    v1 = np.array([b_rad * math.sin(pa_rad), b_rad * math.cos(pa_rad)])
    v2 = np.array([-s_ra_off * math.cos(dec_rad), -s_dec_off])
    v1mag = math.sqrt(v1 @ v1)
    if v1mag == 0.0:
        raise ValueError("offset_prediction requires a nonzero b_arcsec")
    v2mag = math.sqrt(v2 @ v2)
    if v2mag == 0.0:
        raise ValueError("offset_prediction requires a nonzero offset")
    v3 = (v1 @ v2) * (v1 / v1mag) / v1mag
    v4 = v1 - v3
    b_new = math.sqrt(v4 @ v4) * 180.0 * 3600.0 / math.pi
    cos_theta_b = max(-1.0, min(1.0, (v1 @ v2) / (v1mag * v2mag)))
    d_rad = v2mag * math.sin(math.acos(cos_theta_b))
    d_km = distance_au * physicalData.AU_KM * math.tan(d_rad)
    delta_t_sec = d_km / sky_velocity_kms
    if retrograde:
        delta_t_sec = -delta_t_sec
    signed = -delta_t_sec if ra_off.to_value(u.rad) < 0 else delta_t_sec
    t_new = t0 + signed * u.s
    pa_new = pa_deg + 180.0 if (v1 @ v4) < 0 else pa_deg
    if pa_new > 360.0:
        pa_new -= 360.0
    return OffsetResult(coord=new_star, time=t_new, b_arcsec=b_new,
                        pa_deg=pa_new)


