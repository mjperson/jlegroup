"""Regression benchmarks: CE97 forward model vs EY92-generated reference light curves.

Reference curves were generated by the Mathematica jleGroup occLightCurves `olcOneLimb2`
model (EY92 family) at lambda = 0.7 um; see tests/data/README.md for provenance.

Tolerances encode the validated 2026-07-02 results (with modest headroom for
dependency drift):

    iso-clear      measured max|res| = 4.24e-5   (T ~ r^0, isothermal)
    shallow-clear  measured max|res| = 9.72e-5   (T ~ r^-0.5)
    steep-clear    measured max|res| = 1.12e-3   (T ~ r^-4.5)

The steep-clear residual is dominated by the REFERENCE's first-order-in-1/lambda EY92
truncation (olcEYorderForOneOverLambda = 1), not by CE97: the neglected O(lambda^-2)
dtheta term has coefficient (9 - 34b + 25b^2)/128 = 5.221 at b = -4.5, which predicts
the observed deep-region plateau. Its tolerance is therefore documentary, not a claim
of 1e-4 agreement.
"""
import os

import numpy as np
import pandas as pd
import pytest

from jlegroup import CE97, physicalData

DATA = os.path.join(os.path.dirname(__file__), "data")

# geometry shared by all bundled cases (see tests/data/<case>/parameters.csv)
B_KM = 900.0                            # closest approach in the observer plane
V_KMS = 25.0                            # shadow velocity
D_KM = 30 * physicalData.AU_KM          # observer distance
LAMBDA_UM = 0.7


def _mfloat(s):
    """Parse Mathematica-formatted floats like 1.086*10^22."""
    return float(str(s).replace("*10^", "e").replace("*^", "e"))


def _load(case):
    d = os.path.join(DATA, case)
    atm = pd.read_csv(os.path.join(d, "atmosphere.csv"))
    atm.columns = [c.strip() for c in atm.columns]
    radius = atm["Radius (km)"].apply(_mfloat).to_numpy()
    numdens = atm["Number Density (/m^3)"].apply(_mfloat).to_numpy()
    lc = pd.read_csv(os.path.join(d, "lightcurve.csv"))
    t = lc["Time (seconds)"].to_numpy(dtype=float)
    flux = lc["Flux (normalized)"].to_numpy(dtype=float)
    return radius, numdens, t, flux


def run_case(radius, numdens, t):
    # Validation vintage: the references were generated with the jleGroup
    # CODATA-1986 Loschmidt; keeps the README residuals exact.
    nu = physicalData.refractivity(numdens, gas="N2", wavelength_um=LAMBDA_UM,
                                   constants=physicalData.CODATA1986)
    position = np.sqrt(B_KM**2 + (V_KMS * t) ** 2)
    model = CE97.ChamberlainElliot1997Model(
        refractivityProfile=nu,
        radialDistance=radius,
        planetDistance=D_KM,
        position=position,
    )
    model.main()
    # Rays above the mapped atmosphere top are clamped to flux = 1 (vacuum)
    # by the model itself since v0.11.0 (the above-atmosphere vacuum clamp;
    # boundary exposed as model.yTop) — the manual idiom is retired.
    assert model.yTop is not None and model.yTop > radius.max() - 5.0
    return np.asarray(model.focusedFlux, dtype=float)


@pytest.mark.parametrize(
    "case,tol",
    [
        ("iso-clear", 1.0e-4),
        ("shallow-clear", 1.5e-4),
        ("steep-clear", 1.3e-3),  # reference truncation; see module docstring
    ],
)
def test_clear_benchmark(case, tol):
    radius, numdens, t, ref = _load(case)
    flux = run_case(radius, numdens, t)
    maxres = float(np.max(np.abs(flux - ref)))
    print(f"{case}: max|residual| = {maxres:.3e} (tol {tol:g})")
    assert maxres < tol, f"{case}: max|residual| = {maxres:.3e} >= {tol:g}"
